void getVerticalMatrix()
{
  FILE *pont_arq;
  pont_arq = fopen("nathan_vertical.txt", "w");

  int i, j;

  fprintf(pont_arq, "matriz = [\n");

  for (i = lines - 1; i >= 0; i--)
  {
    fprintf(pont_arq, "    ");
    for (j = 0; j < columns; j++)
    {
      if (j == columns - 1)
        fprintf(pont_arq, "%d ;\n", matrix[i][j]);
      else
        fprintf(pont_arq, "%d, ", matrix[i][j]);
    }
  }
  fprintf(pont_arq, "]");
}

void getHorizontalMatrix()
{
  FILE *pont_arq;
  pont_arq = fopen("nathan_horizontal.txt", "w");

  int i, j;

  fprintf(pont_arq, "matriz = [\n");

  for (i = 0; i < lines; i++)
  {
    fprintf(pont_arq, "    ");
    for (j = columns - 1; j >= 0; j--)
    {
      if (j == 0)
        fprintf(pont_arq, "%d ;\n", matrix[i][j]);
      else
        fprintf(pont_arq, "%d, ", matrix[i][j]);
    }
  }
  fprintf(pont_arq, "]\n");
}

